#include <iostream>
#include <vector>
using namespace std;

int main() {
    unsigned long long n; // generators count
    unsigned long long m; // events count

    vector<vector<unsigned long long>> geners();

    for(int i = 0; i < m; i++) {
        for(int j = 0; j < n; j++) {
            cout << geners[i][j] << ' ';
        }
        cout << '\n';
    }
}
